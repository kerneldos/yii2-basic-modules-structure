<?php

namespace app\modules\site\controllers\frontend;

use app\components\BaseModule;
use Yii;
use yii\web\Response;
use app\controllers\AppController;

use app\modules\site\models\SignupForm;
use app\modules\site\models\LoginForm;
use app\modules\site\models\ContactForm;
use app\modules\site\events\UserEvent;

/**
 * Default controller for the `site` module
 */
class DefaultController extends AppController
{
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Renders the index view for the module
     * @return string
     */
    public function actionIndex()
    {
        return $this->render('index');
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        $this->layout = 'login';

        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();

        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            Yii::$app->trigger(BaseModule::EVENT_USER_AFTER_LOGIN, new UserEvent([
                'user' => Yii::$app->user->identity,
            ]));

            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Signs user up.
     *
     * @return mixed
     * @throws \yii\base\Exception
     */
    public function actionSignup()
    {
        $model = new SignupForm();
        if ($model->load(Yii::$app->request->post())) {
            if (($user = $model->signup()) != false) {
                Yii::$app->session->setFlash('success', 'Thank you for registration. Please check your inbox for verification email.');

                Yii::$app->trigger(BaseModule::EVENT_USER_AFTER_SIGNUP, new UserEvent([
                    'user' => $user,
                ]));

                return $this->goHome();
            }
        }

        return $this->render('signup', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }
}
